DROP TABLE IF EXISTS "genre" CASCADE;
DROP TABLE IF EXISTS "film" CASCADE;
DROP TABLE IF EXISTS "film_genre" CASCADE;
DROP TABLE IF EXISTS "rating" CASCADE;
DROP TABLE IF EXISTS "user" CASCADE;
DROP TABLE IF EXISTS "user_film_liked" CASCADE;
DROP TABLE IF EXISTS "user_friend" CASCADE;
DROP TABLE IF EXISTS "reviews" CASCADE;
DROP TABLE IF EXISTS "user_review_scored" CASCADE;

CREATE TABLE IF NOT EXISTS "genre"
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS "rating"
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS "film"
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    description TEXT,
    release_date date,
    duration INTEGER,
    rating_id INTEGER NOT NULL REFERENCES "rating" (id)
);

CREATE TABLE IF NOT EXISTS "user"
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL,
    login TEXT NOT NULL,
    name TEXT,
    birthday DATE
);

CREATE TABLE IF NOT EXISTS "film_genre"
(
    film_id INTEGER NOT NULL REFERENCES "film" (id),
    genre_id INTEGER NOT NULL REFERENCES "genre" (id)
);

CREATE TABLE IF NOT EXISTS "user_film_liked"
(
    user_id INTEGER NOT NULL REFERENCES "user" (id),
    film_id INTEGER NOT NULL REFERENCES "film" (id)
);

CREATE TABLE IF NOT EXISTS "user_friend"
(
    user_id INTEGER NOT NULL REFERENCES "user" (id),
    friend_id INTEGER NOT NULL REFERENCES "user" (id),
    is_accept BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS "reviews"
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content TEXT NOT NULL,
    is_positive BOOLEAN NOT NULL,
    user_id INTEGER NOT NULL REFERENCES "user" (id),
    film_id INTEGER NOT NULL REFERENCES "film" (id)
);

CREATE TABLE IF NOT EXISTS "user_review_scored"
(
    user_id INTEGER NOT NULL REFERENCES "user" (id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    review_id INTEGER NOT NULL REFERENCES "reviews" (id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    score INTEGER NOT NULL
);

ALTER TABLE "user_review_scored"
ADD CONSTRAINT "uq_user_review"
    UNIQUE ("review_id", "user_id");


